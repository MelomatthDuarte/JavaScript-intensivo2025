// Operadores de asignacion
// Operador de asignacion simple

// Asignacion de suma
let y = 10;
y += 5;
console.log(y);

// Asignacion de resta
let z = 15;
z -= 7;
console.log(z);

// Asignacion de multiplicacion
let w = 20;
w *= 2;
console.log(w);

// Asignacion de division
let a = 50;
a /= 5;
console.log(a);

// Asignacion de modulo
let b = 17;
b %= 5;
console.log(b);

// Asignacion de exponencial
let c = 2;
c **= 3;
console.log(c);
