// Metodo pop:
const frutas = ['manzana', 'banana', 'mango'];
const frutaEliminada = frutas.pop();
console.log(frutaELiminada);
console.log(frutas);

// Metodo shift:
const colores = ['rojo', 'azul', 'negro', 'verde'];
const colorEliminado = colores.shift();
console.log(colorEliminado);
console.log(colores);

// Metodo splice:
const numeros = [1, 2, 3, 4, 5];
const numerosEliminados = numeros.splice(2, 2);
console.log(numerosEliminados);
console.log(numeros);

// Metodo slice:
const autos = ['Nissa', 'Toyota', 'Chevy', 'BMW'];
const autosEliminados = autos.slice(1, 3);
console.log(autosEliminados);
console.log(autos);

